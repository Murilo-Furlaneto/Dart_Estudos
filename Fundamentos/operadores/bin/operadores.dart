void main() {
  //Aritméticos
  int a = 7;
  int b = 3;
  int resultado = a + b;
  int resultadoSub = a - b;
  int resultadoMult = a * b;
  double resultadoDiv = a / b;

  print("O resultado da soma é: $resultado");
  print("O resultado da subtração é: $resultadoSub");
  print("O resultado da soma é: $resultadoMult");
  print("O resultado da soma é: $resultadoDiv");

  // Operadores lógicos
  bool c = true;
  bool d = false;

  print(c && d); // E

  print(c || d); // OU

  print(c ^ d); // OU Exclusivo
}
